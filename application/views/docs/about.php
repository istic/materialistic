<h1>This is Material.Istic</h1>

<h2>So what is this?</h2>

<p>This is a thing for Crowdfunding Backers to keep track of which crowdfunding campaigns 
still owe them stuff, and also graph how much stuff they've got coming, and how late it really is.</p>

<h2>Why is this?</h2>

<p>I was keeping track of all this stuff on an increasingly complicated spreadsheet, and every
so often I remember that I develop websites for a living, and that any spreadsheet containing thirty
lines of code in cell is past due becoming some kind of real program. So I spent a weekend doing this.</p>

<h2>What's it built on?</h2>

<p>Do you really care? Magic and pixies. Specifically, PHP, MySQL, CodeIgniter, Bootstrap &amp; Bootswatch</p>

