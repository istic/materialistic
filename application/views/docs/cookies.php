        <ul>
            <li>85g (3oz) each of butter & sugar. Has to be butter, marg fails. Beat into submission.</li>
            <li>Add an egg, beat into submission</li>
            <li>Add 170g (6oz) flour (sifted), touch of salt</li>
            <li>Add 30g (1oz) brown sugar</li>
            <li>Add a bit of Vanilla Essence</li>
            <li>Add 115g (4oz) Chocolate (Green & Blacksâ€™ Maya Gold works really well) broken up into teeny-weenie pieces</li>
            <li>Submission: the beating.</li>
            <li>Break off into balls of about an inch (3 or so cm) across, and arrange on a baking tray. They spread out a little bit, so space them out.</li>
